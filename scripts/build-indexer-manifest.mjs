#!/usr/bin/env node
/**
 * Stamp the Python indexer sources with a content hash.
 *
 * The coding background task is bundled to a single JS file and fetched over
 * HTTP (`<origin>/<buildId>/tasks/coding.js`), so the indexer's `.py` files
 * cannot ride inside it. They ship instead as static assets under
 * `client/public/coding-agent/indexer/`, which the app serves at its origin root
 * in dev (vite), Node, and the Cloudflare Worker alike.
 *
 * This script writes two things:
 *
 *   client/public/coding-agent/indexer/manifest.json
 *     { version, files: [{ path, sha256, bytes }] }
 *     Fetched at runtime; every file is verified against its digest before use,
 *     so a truncated download can't leave a half-written daemon on disk.
 *
 *   client/agent/indexer/indexerVersion.ts
 *     `export const INDEXER_VERSION = '<version>'` — baked into the task bundle,
 *     so the task knows which version it wants with zero network. `version` is
 *     the hash of all the file hashes: edit any `.py` and it changes, which
 *     re-materializes into a fresh directory on next run. A deploy therefore
 *     ships a new indexer; nothing has to be rebuilt or reinstalled.
 *
 * Runs from `prebuild` and `predev` so both paths stay in step.
 */
import { createHash } from 'node:crypto';
import { readdirSync, readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const assetDir = join(repoRoot, 'client', 'public', 'coding-agent', 'indexer');
const versionFile = join(
  repoRoot,
  'client',
  'agent',
  'indexer',
  'indexerVersion.ts',
);

const sha256 = (buf) => createHash('sha256').update(buf).digest('hex');

// Sorted so the version is stable across filesystems.
const names = readdirSync(assetDir)
  .filter((n) => n.endsWith('.py') || n === 'requirements.txt')
  .sort();

if (names.length === 0) {
  console.error(`[indexer-manifest] no sources found in ${assetDir}`);
  process.exit(1);
}

const files = names.map((name) => {
  const buf = readFileSync(join(assetDir, name));
  return { path: name, sha256: sha256(buf), bytes: buf.length };
});

// Version = hash of the per-file hashes (not of the concatenated bytes, so a
// rename alone also changes it).
const version = sha256(
  files.map((f) => `${f.path}:${f.sha256}`).join('\n'),
).slice(0, 16);

writeFileSync(
  join(assetDir, 'manifest.json'),
  JSON.stringify({ version, files }, null, 2) + '\n',
);

mkdirSync(dirname(versionFile), { recursive: true });
writeFileSync(
  versionFile,
  `// Auto-generated by scripts/build-indexer-manifest.mjs — do not edit manually.\n` +
    `// Content hash of client/public/coding-agent/indexer/*.py + requirements.txt.\n` +
    `export const INDEXER_VERSION = '${version}';\n`,
);

console.log(
  `[indexer-manifest] version=${version} files=${files.length} ` +
    `bytes=${files.reduce((n, f) => n + f.bytes, 0)}`,
);
